package model;

/**
 *
 * @author Matheus
 */
public class livro {
    private String titulo;
    private String isbn;

    public livro(String titulo, String isbn) {
        this.titulo = titulo;
        this.isbn = isbn;
    }

    public livro() {
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }
}
