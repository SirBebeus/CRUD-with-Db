    private void CADASTRARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CADASTRARActionPerformed
        if(jTextFieldnn.getText().isEmpty() || jTextFieldnF.getText().isEmpty() || jTextFieldData.getText().isEmpty() ){
             JOptionPane.showMessageDialog(null,"CAMPO VAZIO");
        }else{
         
            cozinheiro cozinheiro_tela = new cozinheiro(jTextFieldnn.getText(), jTextFieldnF.getText(), jTextFieldData.getText());
        
            try {
                //categoriabanco precisa da conexao com o banco pra inserir
                Connection conexao = new Conexao().getConnection();
                CozinheiroDAO cozinheirobanco = new CozinheiroDAO(conexao);
                boolean existir = cozinheirobanco.validar(cozinheiro_tela);
                if(existir){
                    JOptionPane.showMessageDialog(null,"Cozinheiro ja cadastrado");
                }
                
                cozinheirobanco.insert(cozinheiro_tela);
                JOptionPane.showMessageDialog(null,"Cozinheiro cadastrado com sucesso!!!");
                readTable();
                jTextFieldnF.setText("");
                jTextFieldnn.setText("");
                jTextFieldData.setText("");


            } catch (SQLException ex) {
                Logger.getLogger(cozinheiroView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }   
        
            
    }//GEN-LAST:event_CADASTRARActionPerformed

