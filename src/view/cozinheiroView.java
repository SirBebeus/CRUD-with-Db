    public void readTable() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        Connection conexao = new Conexao().getConnection();
        CozinheiroDAO cozinheirobanco = new CozinheiroDAO(conexao);
        for(cozinheiro p: cozinheirobanco.selectALL()){
            modelo.addRow(new Object[]{
                p.getNome_fantasia(),
                p.getNome_cozinheiro(),
                p.getDt_contrato()
            });
        }
    }
    public void readDesc(String desc) throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        Connection conexao = new Conexao().getConnection();
        CozinheiroDAO categoriabanco = new CozinheiroDAO(conexao);
        for(cozinheiro p: categoriabanco.readDesc(desc)){
            modelo.addRow(new Object[]{
                p.getNome_fantasia(),
                p.getNome_cozinheiro(),
                p.getDt_contrato()
            });
        }
    }

